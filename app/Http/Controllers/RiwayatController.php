     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if(auth()->user()->role != 'anggota'){
            return redirect()->back()->with('error', 'Anda tidak memiliki akses ke halaman tersebut');
        }

        $transactions = Transaction::where('anggota_id', auth()->user()->relatedAnggota->id)->orderBy('created_at', 'desc')->paginate(10);

        return view('riwayat.index', compact('transactions'));
    }

